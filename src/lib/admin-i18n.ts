// Extract the type excluding function properties for simple string access
type DictEntry = (typeof dict)["en"];
export type AdminDict = {
  [K in keyof DictEntry]: DictEntry[K] extends (...args: never[]) => string
    ? DictEntry[K]
    : string;
};

export function getAdminDict(locale: AdminLocale): AdminDict {
  return dict[locale] as unknown as AdminDict;
}

export function detectAdminLocale(
  cookieValue?: string | null,
  acceptLanguage?: string | null,
): AdminLocale {
  // 1. Explicit preference from cookie
  if (cookieValue && (cookieValue === "en" || cookieValue === "fr" || cookieValue === "zh")) {
    return cookieValue;
  }
  // 2. Browser Accept-Language
  if (acceptLanguage) {
    const primary = acceptLanguage.split(",")[0]?.split("-")[0]?.toLowerCase();
    if (primary === "zh") return "zh";
    if (primary === "fr") return "fr";
    if (primary === "en") return "en";
  }
  // 3. Default
  return "en";
}
