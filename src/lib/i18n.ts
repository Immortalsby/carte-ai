export function getDictionary(language: LanguageCode): Dictionary {
  return { ...en, ...(overrides[language] ?? {}) };
}

export function getLocalizedText(text: LocalizedText, language: LanguageCode) {
  return (
    text[language] ||
    (language === "zh-Hant" ? text.zh : undefined) ||
    text.en ||
    text.fr ||
    text.zh
  );
}
